#se/ se não se  / se não

condicao1 = False
condicao2 = True
condicao3 = True
condicao4 = True

if condicao1:
    print('Esse é o código da condição 1')
elif condicao2:
    print('Esse é o código da condição 2')
elif condicao3:
    print('Esse é o código da condição 3')
elif condicao4:
    print('Esse é o código da condição 4') 
else:
    print('Nenhuma condição foi satisfeita')
    
    
